/**
 * Data Types in JavaScript
 */

// String:
let animal = "Tigers are cute.";
console.log("String:", animal);

// Numbers:
let lions = 10;
console.log("Integer:", lions);

// Floating point:
let apples = 5.5;
console.log("Floating point number:", apples);

// Boolean: 
let cats = true;
console.log("Boolean value:", cats);

// Null value (nothing):
let dogs = null;
console.log("Null value:", dogs);

// Undefined:
let oranges;
console.log("Undefined:", oranges);
